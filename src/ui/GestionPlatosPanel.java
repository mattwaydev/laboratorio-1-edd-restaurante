    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        javax.swing.JTextField tfNombre    = new javax.swing.JTextField();
    javax.swing.JTextField tfCategoria = new javax.swing.JTextField();
    javax.swing.JTextField tfPrecio    = new javax.swing.JTextField();
    javax.swing.JComboBox<String> cbDisp = new javax.swing.JComboBox<>(new String[]{"SI", "NO"});

    Object[] campos = {
        "Nombre:",    tfNombre,
        "Categoria:", tfCategoria,
        "Precio:",    tfPrecio,
        "Disponible:", cbDisp
    };

    int res = JOptionPane.showConfirmDialog(this, campos, "Añadir Plato", JOptionPane.OK_CANCEL_OPTION);
    if (res == JOptionPane.OK_OPTION) {
        try {
            int id        = platoDAO.generarNuevoId();
            String nombre = tfNombre.getText().trim();
            String cat    = tfCategoria.getText().trim();
            double precio = Double.parseDouble(tfPrecio.getText().trim());
            boolean disp  = cbDisp.getSelectedItem().equals("SI");

            platoDAO.agregarPlato(new Plato(id, nombre, cat, precio, disp));
            cargarTabla();
            JOptionPane.showMessageDialog(this, "Plato agregado bien");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingresa un numero valido");
        }
    }

        
    }//GEN-LAST:event_jButton2ActionPerformed
