    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        int fila = jTable1.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un plato de la tabla primero");
        return;
    }

    int id  = (int) jTable1.getValueAt(fila, 0);
    Plato p = platoDAO.buscarPlatoPorId(id);

    javax.swing.JTextField tfNombre    = new javax.swing.JTextField(p.getNombre());
    javax.swing.JTextField tfCategoria = new javax.swing.JTextField(p.getCategoria());
    javax.swing.JTextField tfPrecio    = new javax.swing.JTextField(String.valueOf(p.getPrecio()));
    javax.swing.JComboBox<String> cbDisp = new javax.swing.JComboBox<>(new String[]{"SI", "NO"});
    cbDisp.setSelectedItem(p.isDisponible() ? "SI" : "NO");

    Object[] campos = {
        "Nombre:",    tfNombre,
        "Categoria:", tfCategoria,
        "Precio:",    tfPrecio,
        "Disponible:", cbDisp
    };

    int res = JOptionPane.showConfirmDialog(this, campos, "Editar Plato " + id, JOptionPane.OK_CANCEL_OPTION);
    if (res == JOptionPane.OK_OPTION) {
        try {
            p.setNombre(tfNombre.getText().trim());
            p.setCategoria(tfCategoria.getText().trim());
            p.setPrecio(Double.parseDouble(tfPrecio.getText().trim()));
            p.setDisponible(cbDisp.getSelectedItem().equals("SI"));

            platoDAO.editarPlato(p);
            cargarTabla();
            JOptionPane.showMessageDialog(this, "Plato editado bien");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingresa un numero valido");
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed
